package com.ibrahim.model;

public class BasicPopulationObject {

	private String erkek;
	private String kadýn;
	private String toplam;
	private String ilAdi;

	public String getErkek() {
		return erkek;
	}

	public void setErkek(String erkek) {
		this.erkek = erkek;
	}

	public String getKadýn() {
		return kadýn;
	}

	public void setKadýn(String kadýn) {
		this.kadýn = kadýn;
	}

	public String getToplam() {
		return toplam;
	}

	public void setToplam(String toplam) {
		this.toplam = toplam;
	}

	public String getIlAdi() {
		return ilAdi;
	}

	public void setIlAdi(String ilAdi) {
		this.ilAdi = ilAdi;
	}

}
